namespace Kirin_Tool.Services.USBUpdate
{
    public static class Crc16X25
    {
        public static ushort Calculate(byte[] data)
        {
            ushort crc = 0xFFFF;

            foreach (byte b in data)
            {
                crc ^= b;
                for (int i = 0; i < 8; i++)
                {
                    if ((crc & 0x0001) != 0)
                    {
                        crc = (ushort)((crc >> 1) ^ 0x8408);
                    }
                    else
                    {
                        crc >>= 1;
                    }
                }
            }

            return (ushort)~crc;
        }

        public static byte[] CalculateBytes(byte[] data)
        {
            ushort crc = Calculate(data);
            return System.BitConverter.GetBytes(crc);
        }
    }
}
